#!/usr/bin/env bash

#--[ENSURE DIRECTORY STRUCTURE]-----------------

source "$LIBDIR/reload/ensure-dirs.sh"

#--[MAKE SCRIPTS EXECUTABLE]-------------------

chmod +x $PROJECTS/scripts/* 2>/dev/null || true
chmod +x $TOOLS/bin/* 2>/dev/null || true
chmod +x $TOOLS/bin/lib/* 2>/dev/null || true
chmod +x $BASHRC/bin/* 2>/dev/null || true
chmod +x $LIBDIR/* 2>/dev/null || true
chmod +x $HOME/bin/* 2>/dev/null || true
chmod +x $HOME/bin/sys/* 2>/dev/null || true

#--[SYNC SYMLINKS]-----------------------------

source "$LIBDIR/reload/symlink-farm.sh"

#--[SYNC SSH KEYS]-----------------------------

sync_ssh_public_keys() {
    local public_dir="$HOME/.ssh/public"
    local authorized_keys="$HOME/.ssh/authorized_keys"

    mkdir -p "$public_dir"
    chmod 700 "$public_dir"

    if compgen -G "$public_dir/*.pub" > /dev/null 2>&1; then
        cat "$public_dir"/*.pub > "$authorized_keys"
        chmod 600 "$authorized_keys"
    else
        touch "$authorized_keys"
        chmod 600 "$authorized_keys"
    fi
}

sync_ssh_private_keys() {
    local private_dir="$HOME/.ssh/private"
    local config_dir="$HOME/.ssh/config.d"
    local auto_config="$config_dir/auto-private-keys"

    mkdir -p "$private_dir" "$config_dir"
    chmod 700 "$private_dir" "$config_dir"

    # Regenerate config with all private keys
    {
        echo "# Auto-generated by lushrc - do not edit manually"
        echo "Host *"
        if compgen -G "$private_dir/*" > /dev/null 2>&1; then
            for key in "$private_dir"/*; do
                # Skip .pub files and directories
                [[ -f "$key" && ! "$key" =~ \.pub$ ]] && echo "    IdentityFile $key"
            done
        fi
    } > "$auto_config"
    chmod 600 "$auto_config"
}

sync_ssh_public_keys
sync_ssh_private_keys

#--[SYNC MIME DEFAULTS]------------------------

source "$LIBDIR/reload/sync-mime-defaults.sh"
ln -sf "$BASHRC/modules/defaults/mimeapps.list" "$HOME/.config/mimeapps.list"

#--[SYSTEM-LEVEL SYNC]-------------------------

if [[ "$1" == "--system" ]] || [[ "$1" == "-s" ]] || [[ "$1" == "--hard" ]] || [[ "$1" == "-h" ]]; then
    sync_system_links
fi

#--[ENSURE KITTY TERMINFO]---------------------

if ! infocmp xterm-kitty &>/dev/null; then
    if ! sat install kitty-terminfo:sys 2>/dev/null; then
        export TERM=xterm-256color
    fi
fi

#--[REFRESH COMMAND HASH]-----------------------

hash -r
